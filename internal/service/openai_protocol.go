package service

func BuildPrompt(transcript string) string {
	return `
Ты помощник руководителя и секретарь встреч.

Тебе передан transcript встречи на русском языке.
Сформируй структурированный протокол встречи.

Верни результат строго в 4 блоках:

1. Краткое резюме
2. Принятые решения
3. Поручения
4. Неопределенности и спорные места

Правила:
- Не выдумывай факты.
- Если неясно, кто ответственный, так и укажи.
- Если неясен срок, так и укажи.
- Пиши по-русски.
- Поручения формулируй кратко и по делу.
- Если решений или поручений нет, явно напиши, что не выявлены.
Transcript:
` + transcript
}
